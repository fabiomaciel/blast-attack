#include "hitbox.h"

#include <SDL.h>
#include <assert.h>

void
hitbox_draw(hitbox_t* hitbox, SDL_Renderer* renderer)
{
  SDL_Rect rect = {
    .x = hitbox->x, .y = hitbox->y, .w = hitbox->width, .h = hitbox->height
  };

  SDL_SetRenderDrawColor(renderer, 0xFF, 0, 0, 1);
  SDL_RenderFillRect(renderer, &rect);
}
